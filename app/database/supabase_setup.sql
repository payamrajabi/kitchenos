create table if not exists public.recipes (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  image_url text,
  image_urls jsonb,
  image_focus_y smallint,
  notes text,
  ingredients text,
  instructions text,
  source_url text,
  servings integer,
  prep_time_minutes integer,
  cook_time_minutes integer,
  total_time_minutes integer,
  calories integer,
  protein_grams integer,
  fat_grams integer,
  carbs_grams integer,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.ingredients (
  id bigint generated by default as identity primary key,
  name text not null unique,
  full_item_name text,
  full_item_name_alt text,
  current_stock text,
  minimum_stock text,
  maximum_stock text,
  category text,
  grocery_category text,
  price numeric,
  preferred_vendor text,
  brand_or_manufacturer text,
  notes text,
  ingredients_text text,
  food_type text default 'generic' check (food_type in ('generic', 'branded', 'custom')),
  barcode text,
  nutrition_fetched_at timestamptz,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.ingredient_nutrients (
  ingredient_id bigint not null references public.ingredients(id) on delete cascade,
  nutrient_id   integer not null,
  nutrient_name text not null,
  value         real not null,
  unit          text not null,
  primary key (ingredient_id, nutrient_id)
);

create table if not exists public.ingredient_portions (
  id            bigint generated by default as identity primary key,
  ingredient_id bigint not null references public.ingredients(id) on delete cascade,
  gram_weight   real not null check (gram_weight > 0),
  description   text not null,
  source        text,
  is_default    boolean not null default false,
  created_at    timestamptz default now()
);

create table if not exists public.equipment (
  id bigint generated by default as identity primary key,
  name text not null unique,
  category text,
  has_item boolean not null default false,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

