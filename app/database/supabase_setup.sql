create table if not exists public.shopping_items (
  id bigint generated by default as identity primary key,
  ingredient_id bigint references public.ingredients(id) on delete set null,
  name text not null,
  quantity text,
  unit text,
  store text,
  aisle text,
  notes text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.people (
  id bigint generated by default as identity primary key,
  name text not null,
  birth_date date,
  weight numeric,
  height text,
  daily_calorie_expenditure numeric,
  calorie_min numeric,
  calorie_max numeric,
  calorie_target numeric,
  protein_min_grams numeric,
  protein_max_grams numeric,
  protein_target_grams numeric,
  fat_min_grams numeric,
  fat_max_grams numeric,
  fat_target_grams numeric,
  carb_min_grams numeric,
  carb_max_grams numeric,
  carb_target_grams numeric,
  dietary_restrictions jsonb,
  allergies jsonb,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table if not exists public.recipe_ingredients (
  recipe_id bigint not null references public.recipes(id) on delete cascade,
  ingredient_id bigint not null references public.ingredients(id) on delete cascade,
  amount text,
  unit text,
  created_at timestamptz default now(),
  primary key (recipe_id, ingredient_id)
);

